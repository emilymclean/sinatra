val TransColorSchemeContrast = ColorSchemeContrast(
    default = ColorSchemePair(
        light = lightScheme,
        dark = darkScheme
    ),
    medium = null,
    high = null
)

@Composable
fun transColorScheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    contrastSetting: ContrastSetting = ContrastSetting.DEFAULT
): ColorScheme =
    TransColorSchemeContrast.pickColorSchemeOption(darkTheme, contrastSetting)

